package Section1.String;

public class IntToStringEx {
    public static void main(String args[]){
        // i = 정수형 데이터 타입, s = 문자열 데이터 타입
        int i = 5;
        String s = Integer.toString(i);
        System.out.println(i+10); // 5 + 10 = 15
        System.out.println(s+10); // "5" + "10" = "510"
    }
}
